#include <iostream>
using namespace std;

int main() {
	// Array Variables
	int numbers[5] = {1, 2, 3, 4, 5};
	char letters[5] = {'a', 'b', 'c', 'd', 'e'};

	cout << "Numbers: " << numbers[0] << ", " << numbers[1] << ", " << numbers[2] << ", " << numbers[3] << ", " << numbers[4] << endl;
	cout << "Letters: " << letters[0] << ", " << letters[1] << ", " << letters[2] << ", " << letters[3] << ", " << letters[4] << endl;
}